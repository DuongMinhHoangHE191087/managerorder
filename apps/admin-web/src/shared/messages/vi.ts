type RepairableValue = string | number | boolean | null | undefined | RepairableValue[] | { [key: string]: RepairableValue };

const UTF8_DECODER = new TextDecoder("utf-8");

function decodeLatin1ToUtf8(value: string): string {
  const bytes = new Uint8Array(value.length);
  for (let index = 0; index < value.length; index += 1) {
    bytes[index] = value.charCodeAt(index) & 0xff;
  }
  return UTF8_DECODER.decode(bytes);
}

function repairVietnameseText(value: string): string {
  const upperDToken = "__VI_UPPER_D__";
  const lowerDToken = "__VI_LOWER_D__";
  const replacement = String.fromCharCode(0xfffd);
  const upperDSequence = replacement + String.fromCharCode(0x1e) + String.fromCharCode(0x90);
  const upperDAmbiguousSequence = replacement + String.fromCharCode(0x1e);
  const lowerDSequence = replacement + String.fromCharCode(0x18);

  let output = decodeLatin1ToUtf8(value);
  output = output
    .split(upperDSequence).join(upperDToken)
    .split(upperDAmbiguousSequence).join(upperDToken)
    .split(lowerDSequence).join(lowerDToken);
  output = decodeLatin1ToUtf8(output);
  output = output
    .split(upperDToken).join(String.fromCharCode(0x110))
    .split(lowerDToken).join(String.fromCharCode(0x111));
  return output;
}

function repairVietnameseValue<T>(value: T): T {
  if (typeof value === "string") {
    return repairVietnameseText(value) as T;
  }

  if (Array.isArray(value)) {
    return value.map((item) => repairVietnameseValue(item)) as T;
  }

  if (value && typeof value === "object") {
    const repaired: Record<string, RepairableValue> = {};
    for (const [key, child] of Object.entries(value as Record<string, RepairableValue>)) {
      repaired[key] = repairVietnameseValue(child);
    }
    return repaired as T;
  }

  return value;
}

