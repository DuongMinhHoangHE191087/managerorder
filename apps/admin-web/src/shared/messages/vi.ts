function encodeCp1252LikeBytes(value: string) {
  const bytes: number[] = [];

  for (const char of value) {
    const codePoint = char.codePointAt(0);
    if (codePoint === undefined) {
      continue;
    }

    if (codePoint <= 0xff) {
      bytes.push(codePoint);
      continue;
    }

    const mappedByte = CP1252_UNICODE_TO_BYTE.get(codePoint);
    if (mappedByte !== undefined) {
      bytes.push(mappedByte);
      continue;
    }

    bytes.push(...new TextEncoder().encode(char));
  }

  return Uint8Array.from(bytes);
}

function decodeCp1252ToUtf8(value: string) {
  return UTF8_DECODER.decode(encodeCp1252LikeBytes(value));
}

function getMojibakeScore(value: string) {
  const replacementCount = (value.match(/\uFFFD/g) ?? []).length * 4;
  const controlCount = (value.match(/[\u0000-\u0008\u000b\u000c\u000e-\u001f]/g) ?? []).length * 4;
  const suspiciousSequenceCount =
    (
      value.match(
        /(?:Ã.|Â.|Ä.|Å.|Æ.|á[º»¼½¾¿]|â€|â€™|â€œ|â€¢|â€“|â€”|Ò.|Ó.|Ô.|Õ.)/g,
      ) ?? []
    ).length;

  return replacementCount + controlCount + suspiciousSequenceCount;
}

function repairVietnameseText(value: string): string {
  let best = value;
  let bestScore = getMojibakeScore(value);

  for (let iteration = 0; iteration < 3; iteration += 1) {
    const candidate = decodeCp1252ToUtf8(best);
    const candidateScore = getMojibakeScore(candidate);

    if (candidate === best || candidateScore > bestScore) {
      break;
    }

    best = candidate;
    bestScore = candidateScore;

    if (candidateScore === 0) {
      break;
    }
  }

  return best;
}

function repairVietnameseValue<T>(value: T): T {
  if (typeof value === "string") {
    return repairVietnameseText(value) as T;
  }

  if (typeof value === "function") {
    const original = value as RepairableFunction;
    const repaired = ((...args: readonly unknown[]) =>
      repairVietnameseValue(original(...args))) as RepairableFunction;
    return repaired as T;
  }

  if (Array.isArray(value)) {
    return value.map((item) => repairVietnameseValue(item)) as T;
  }

  if (value && typeof value === "object") {
    const repaired: Record<string, RepairableValue> = {};
    for (const [key, child] of Object.entries(value as Record<string, RepairableValue>)) {
      repaired[key] = repairVietnameseValue(child);
    }
    return repaired as T;
  }

  return value;
}

